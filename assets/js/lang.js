const dictKh = {};
for (let key in dictEn) {
    dictKh[dictEn[key]] = key;
}

let currentLang = localStorage.getItem('lang') || 'kh';
// A Set to mark elements already processed to avoid infinite loop on reverse mapping if strings match.
// But matching is exact, so usually fine unless EN text matches KH text.

function walkNode(node, dict, fromDict) {
    // Replace text nodes
    if (node.nodeType === 3) {
        let text = node.nodeValue.trim();
        if (text && fromDict[text]) {
            node.nodeValue = node.nodeValue.replace(text, dict[fromDict[text]]);
        }
    } else if (node.nodeType === 1) { // Element node
        if (node.id === "langDropdownUl" || node.id === "currentLang") return; // Skip translating the lang select dropdown itself

        if (node.placeholder && fromDict[node.placeholder.trim()]) {
            node.placeholder = dict[fromDict[node.placeholder.trim()]];
        }
        for (let i = 0; i < node.childNodes.length; i++) {
            walkNode(node.childNodes[i], dict, fromDict);
        }
    }
}

function setLang(lang) {
    if (lang === currentLang) return;

    let toDict = lang === 'en' ? dictEn : dictKh;
    let fromDict = lang === 'en' ? dictKh : dictEn;



    let mapToUse = lang === 'en' ? dictEn : dictKh;

    // A modified walkNode using a single map
    function applyMap(n, map) {
        if (n.nodeType === 3) {
            let t = n.nodeValue.trim();
            if (t && map[t] !== undefined) {
                n.nodeValue = n.nodeValue.replace(t, map[t]);
            }
        } else if (n.nodeType === 1) {
            if (n.id === "langDropdownUl" || n.id === "currentLang") return;
            if (n.placeholder && map[n.placeholder.trim()] !== undefined) {
                n.placeholder = map[n.placeholder.trim()];
            }
            for (let i = 0; i < n.childNodes.length; i++) {
                applyMap(n.childNodes[i], map);
            }
        }
    }

    applyMap(document.body, mapToUse);
    localStorage.setItem('lang', lang);
    currentLang = lang;

    const langBtn = document.getElementById('currentLang');
    if (langBtn) {
        langBtn.innerText = lang === 'en' ? 'English' : 'ភាសាខ្មែរ';
    }
}

document.addEventListener("DOMContentLoaded", () => {
    // Inject lang dropdown
    const navUl = document.querySelector('.navbar-nav');
    if (navUl && !document.getElementById('langDropdownLi')) {
        const li = document.createElement('li');
        li.className = 'nav-item dropdown ms-lg-3';
        li.id = 'langDropdownLi';
        li.innerHTML = `
            <a class="nav-link dropdown-toggle" href="#" role="button" data-bs-toggle="dropdown" aria-expanded="false">
                 <span id="currentLang">ភាសាខ្មែរ</span>
            </a>
            <ul class="dropdown-menu" id="langDropdownUl">
                <li><a class="dropdown-item" href="javascript:void(0)" onclick="setLang('kh')">ភាសាខ្មែរ</a></li>
                <li><a class="dropdown-item" href="javascript:void(0)" onclick="setLang('en')">English</a></li>
            </ul>
            <link href="https://fonts.googleapis.com/css2?family=Battambang:wght@100;300;400;700;900&display=swap"
        `;
        navUl.appendChild(li);
    }

    // Auto translate if stored lang is english
    if (currentLang === 'en') {
        const tempLang = currentLang;
        currentLang = 'kh'; // Trick setLang to run translation from KH to EN
        setLang(tempLang);
    }
});
